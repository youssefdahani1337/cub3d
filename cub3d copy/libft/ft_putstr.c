#include "libft.h"

void	ft_putstr(char *s, int c)
{
	int	i;

	if (c == 0)
		write(2, "Error\n", 6);
	i = 0;
	if (!s)
		return ;
	while (s[i])
		i++;
	write(1, s, i);
	write(1, "\n", 1);
}
